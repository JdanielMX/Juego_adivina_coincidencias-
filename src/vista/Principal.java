    private void imprimirMatriz(String[][] matriz) {

        for (int f = 0; f < 4; f++) {

            for (int c = 0; c < 4; c++) {

                txtGrafico.setText(txtGrafico.getText() + matriz[f][c] + "         ");

            }

            txtGrafico.setText(txtGrafico.getText() + "\n");

        }


    }
