    public void evaluar(String emoji) {
        String frase;
        PrincipalController pr = new PrincipalController();

        if (!txtCantidad.getText().isBlank()) {

            int cantidadAparece = Integer.parseInt(txtCantidad.getText());

            if (cantidadAparece < 0 || cantidadAparece > 16) {

                JOptionPane.showMessageDialog(null, "La cantidad de veces debe estar entre 0 y 16");
            } else {

                String[][] matriz = pr.rellenarMatriz();
                int resultado = pr.determinaCantidadEmoji(emoji, cantidadAparece, matriz);

                if (resultado == cantidadAparece) {

                    frase = "Ganaste";

                } else {

                    frase = "No acertaste";
                }

                lblElejido.setText("Elejido: " + emoji);
                lblEncontrados.setText("Encontrados: " + resultado);
                lblResultado.setText("Resultado: " + frase);
                imprimirMatriz(matriz);

            }

        } else {

            JOptionPane.showMessageDialog(null, "No ingresaste ningun valor");
        }

    }

